export const currentLanguage = 'ar';

const findNestedTranslation = (obj: any, parts: string[]): string => {
  if (parts.length === 0) return '';
  
  const [current, ...rest] = parts;
  
  if (rest.length === 0) {
    return typeof obj[current] === 'string' ? obj[current] : '';
  }

  if (obj[current] && typeof obj[current] === 'object') {
    return findNestedTranslation(obj[current], rest);
  }

  return '';
};

export function t(key: string): string {
  const language = currentLanguage;
  const keyParts = key.split('.');
  const translation = findNestedTranslation(translations[language], keyParts);
  
  if (!translation && language !== 'ar' && translations.ar) {
    // Fallback to Arabic if translation doesn't exist in current language
    const fallbackTranslation = findNestedTranslation(translations.ar, keyParts);
    if (fallbackTranslation) return fallbackTranslation;
  }
  
  return translation || key;
}

export function useTranslation() {
  // نسخة Hook من وظيفة الترجمة
  return { t };
}

export function formatWithTranslation(key: string, replacements: Record<string, string | number>): string {
  let translation = t(key);
  
  Object.entries(replacements).forEach(([placeholder, value]) => {
    translation = translation.replace(`{${placeholder}}`, String(value));
  });
  
  return translation;
}